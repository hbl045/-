<script>
import router from '@/router/router-static'
export default {
  mounted() {
    this.init();
  },
  methods:{
    init() {
      if (this.$storage.get('Token')) {
        //返回上次关闭的页面
        this.$http({
          url: `${this.$storage.get('sessionTable')}/session`,
          method: "get"
        }).then(({data}) => {
          //这里coke不为零指token过期，重新登陆
          if(data && data.code != 0){
            router.push({ name: 'login'})
          }
        });
      } else {
        //刚登陆没有token一定会进入去login
          router.push({name: 'login'})
      }
      // 基于准备好的dom，初始化echarts实例
    }
  }
};
</script>

